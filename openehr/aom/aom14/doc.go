// Package aom14 holds Go types generated from the openEHR Archetype
// Object Model 1.4 (BMM source: openehr_am_1.4.0.bmm.json +
// openehr_base_1.3.0.bmm.json).
//
// # Layout
//
// This package sits next to [openehr/rm], not under
// [openehr/template]. Per
// docs/plans/2026-05-15-bmm-codegen.md Phase 4 (referenced from
// docs/specifications/bmm-conformance.md § Schema → Go package set): AOM is the
// model of an archetype; templates consume archetypes (an OPT
// contains flattened archetype definitions). The future
// openehr/template/ parser will import this package, but does not
// own it.
//
// # Base-type sharing with openehr/rm
//
// AOM 1.4 includes openehr_base_1.3.0 by reference, the same way RM
// does. The generator does NOT duplicate base classes here; instead,
// references to base classes (e.g. ARCHETYPE_ID, HIER_OBJECT_ID,
// AUTHORED_RESOURCE, VALIDITY_KIND) are emitted as rm.<GoName> with
// a one-way import on openehr/rm. This is Option C from the Phase 4
// design — picked to avoid binary bloat and to keep the
// rm.HierObjectID identity stable when AOM-side code interoperates
// with RM-side code.
//
// The reverse direction (rm importing aom14) is prohibited; AOM
// types are never referenced from RM.
//
// # Type registry
//
// Concrete AOM classes register with the shared
// [openehr/rm/typereg].Default registry at package-init time. The
// _type discriminator strings are disjoint between RM and AOM
// (no DV_QUANTITY in AOM, no ARCHETYPE in RM), so the two models
// coexist in a single registry. The generator emits typereg_gen.go
// here for the AOM side.
//
// # Hand-written companions
//
// Files named *_ext.go are reserved for hand-written method bodies
// and helpers. The generator never touches them. Generated files
// carry the standard "// Code generated by bmmgen; DO NOT EDIT."
// header and MUST NOT be hand-edited (REQ-042, REQ-044).
